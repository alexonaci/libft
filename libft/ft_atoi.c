int		ft_atoi(const char *str)
{
	int		i;
	int		rez1;
	int		rez2;
	int		n;

	i = 0;
	rez1 = 0;
	rez2 = 0;
	n = 1;
	while (str[i] == 32 || (str[i] >= 9 && str[i] <= 13))
		i++;
	if (str[i] == '-')
	{
		n = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		rez1 = rez1 * 10 + str[i] - '0';
		i++;
	}
	rez2 = rez1 * n;
	return (rez2);
}
