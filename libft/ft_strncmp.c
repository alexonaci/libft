#include <string.h>

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (n--)
	{
		if (*s2 != *s1)
			return (*s1 - *s2);
		else
		{
			s2++;
			s1++;
		}
	}
	return (0);
}
